public class JavaVariables {
    public static void main(String[] args) {
        int a = 10;
        int b = 49;
        String name = "rakesh Vishwakarma";
        System.out.println(name);
        System.out.println(b);
        System.out.println(a);
        
        a = 34;
        System.out.println(a);

        byte c = 45;
        System.out.println(c);
        String ch = "r";
        System.out.println(ch);   
    }
}

